import { browser, by, element } from 'protractor';

export class Notes {
  navigateTo() {
    return browser.get('/notes');
  }

  // get the app name
  getWelcome() {
    return element(by.css('h1 _ngcontent-c3')).getText();
  }

  // get the list of todos
  getTodos() {
    return element(by.css('app-note-list'));
  }

  // add a todo note
  addTodo() {
    return element(by.css('input.new-note ng-pristine ng-valid ng-touched'));
  }

  // delete a todo note
  deleteTodo() {
    return element(by.buttonText('destroy')).click();
  }
}
